class CheckCompliance:
    def __init__(self):
        compliance_prompt = ChatPromptTemplate.from_messages(
            [
                ("system", COMPLIANCE_SYSTEM_PROMPT),
                ("human", "# 口播文案：\n {script}")
            ]
        )
        llm_with_structured_output = llm_regular.with_structured_output(ComplianceResult)
        self.compliance_chain = compliance_prompt|llm_with_structured_output

    async def __call__(self, script:str)-> ComplianceResult:
        return await self.compliance_chain.ainvoke({'script': script})

